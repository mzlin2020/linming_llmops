SUMMARIZER_TEMPLATE = """逐步总结提供的对话内容，在之前的总结基础上继续添加并返回一个新的总结，并确保新总结的长度不要超过2000个字符，必要的时候可以删除一些信息，尽可能简洁。

EXAMPLE
当前总结:
人类询问 AI 对人工智能的看法。AI 认为人工智能是一股向善的力量。

新的会话:
Human: 为什么你认为人工智能是一股向善的力量？
AI: 因为人工智能将帮助人类发挥他们全部的潜力。

新的总结:
人类询问AI对人工智能的看法，AI认为人工智能是一股向善的力量，因为它将帮助人类发挥全部潜力。
END OF EXAMPLE

当前总结:
{summary}

新的会话:
{new_lines}

新的总结:"""


# 会话自动命名：据用户在一轮里的第一句话，为本次会话起一个简短标题。{query} 直接作为 human 内容传入。
CONVERSATION_NAME_TEMPLATE = """你是一个会话标题生成助手。下面会给你用户在一次对话里说的第一句话，请为这次会话起一个简短贴切的标题。

要求：
- 直接输出标题本身，不要任何解释、引号、标点符号或代码块标记。
- 标题不超过 20 个字，概括用户的话题或意图即可。
- 使用与用户输入相同的语言。"""


# 优化人设：把用户填写的 preset_prompt 改写成结构更清晰的角色提示词。
OPTIMIZE_PROMPT_TEMPLATE = """# 角色
你是一位资深的 AI 提示词工程师，擅长根据用户给出的原始描述，优化并补全成一段结构清晰、可直接用于驱动 AI 助手的人设提示词。

## 技能
- 识别用户原始描述的语言与意图，并沿用同一种语言输出。
- 用「# 角色 / ## 技能 / ## 限制」之类的清晰结构组织提示词。
- 补全角色定位、能做什么、语气风格与行为边界，但不要凭空捏造与原意无关的设定。

## 约束
- 只输出优化后的提示词正文本身，不要任何解释、前后缀或代码块标记。
- 使用与用户输入相同的语言。
- 若用户输入很简短，则在尊重原意的前提下做合理扩写。"""


# 开场问题：依据人设，预测用户初次使用时最可能问的问题。
OPENING_QUESTIONS_TEMPLATE = """你是一个问题生成助手。下面会给你一段 AI 助手的人设描述，请预测用户初次使用这个助手时最可能问它的最多 3 个问题。

要求：
- 每个问题简短自然、不超过 30 个字，站在用户口吻提问。
- 问题应贴合该助手的人设与擅长领域。
- 严格只返回一个 JSON 字符串数组，形如 ["问题1", "问题2", "问题3"]，不要输出任何额外文字、解释或代码块标记。
- 若人设为空或信息不足，则返回一个通用问题数组。"""


